import { NextRequest, NextResponse } from 'next/server';
import {
  collection,
  getDocs,
  query,
  where,
  doc,
  updateDoc,
  addDoc,
  writeBatch,
  serverTimestamp,
  Timestamp,
} from 'firebase/firestore';
import { ref, uploadBytes, getDownloadURL } from 'firebase/storage';
import { db, storage } from '@/lib/firebase';

// ─── Constants ────────────────────────────────────────────────────────────────

const RETENTION_DAYS = 15;

// ─── Security: only Vercel cron or our own secret can call this ───────────────

function isAuthorized(req: NextRequest): boolean {
  // Vercel sets this header on cron invocations
  const cronSecret = req.headers.get('authorization');
  const expectedSecret = `Bearer ${process.env.CRON_SECRET}`;

  // Also allow Vercel's internal cron header
  const vercelCron = req.headers.get('x-vercel-cron');

  if (vercelCron === '1') return true;
  if (process.env.CRON_SECRET && cronSecret === expectedSecret) return true;

  // In development, allow without auth
  if (process.env.NODE_ENV === 'development') return true;

  return false;
}

// ─── Helpers ──────────────────────────────────────────────────────────────────

function tsToDate(ts: unknown): Date {
  if (ts instanceof Timestamp) return ts.toDate();
  if (typeof ts === 'string') return new Date(ts);
  return new Date();
}

