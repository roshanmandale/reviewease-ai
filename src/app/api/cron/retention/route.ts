// ─── Core retention logic for one business ────────────────────────────────────

async function processBusinessRetention(businessId: string, businessName: string, ownerUid: string): Promise<{
  businessId: string;
  status: 'success' | 'skipped' | 'error';
  scansDeleted: number;
  clicksDeleted: number;
  reportId?: string;
  error?: string;
}> {
  try {
    const now = new Date();
    const cutoff = new Date(now.getTime() - RETENTION_DAYS * 24 * 60 * 60 * 1000);

    // ── 1. Fetch ALL logs for this business ──
    const [scanSnap, clickSnap] = await Promise.all([
      getDocs(query(collection(db, 'scan_logs'), where('businessId', '==', businessId))),
      getDocs(query(collection(db, 'review_clicks'), where('businessId', '==', businessId))),
    ]);

    // Separate into: within retention window vs old (to delete)
    const recentScans = scanSnap.docs.filter((d) => tsToDate(d.data().timestamp) >= cutoff);
    const oldScans    = scanSnap.docs.filter((d) => tsToDate(d.data().timestamp) < cutoff);
    const recentClicks = clickSnap.docs.filter((d) => tsToDate(d.data().timestamp) >= cutoff);
    const oldClicks    = clickSnap.docs.filter((d) => tsToDate(d.data().timestamp) < cutoff);

    // Skip if nothing to delete
    if (oldScans.length === 0 && oldClicks.length === 0) {
      return { businessId, status: 'skipped', scansDeleted: 0, clicksDeleted: 0 };
    }

    // ── 2. Compute stats for the retention window ──
    const totalScans  = recentScans.length;
    const totalClicks = recentClicks.length;
    const conversionRate = totalScans > 0 ? Math.round((totalClicks / totalScans) * 100) : 0;

    // ── 3. Generate HTML report ──
    const html = generateReportHTML({
      businessName,
      periodStart: cutoff,
      periodEnd: now,
      totalScans,
      totalClicks,
      conversionRate,
      generatedAt: now,
    });

    // ── 4. Upload to Firebase Storage ──
    const fileName = `reports/${ownerUid}/${businessId}/${now.getTime()}.html`;
    const storageRef = ref(storage, fileName);
    const blob = new Blob([html], { type: 'text/html' });
    await uploadBytes(storageRef, blob, { contentType: 'text/html' });
    const pdfUrl = await getDownloadURL(storageRef);

    // ── 5. Save report record to Firestore ──
    const reportRef = await addDoc(collection(db, 'reports'), {
      businessId,
      businessName,
      ownerUid,
      periodStart: cutoff.toISOString(),
      periodEnd: now.toISOString(),
      totalScans,
      totalClicks,
      conversionRate,
      pdfUrl,
      generatedAt: serverTimestamp(),
    });

    // ── 6. Update business retention fields ──
    const nextDeletion = new Date(now.getTime() + RETENTION_DAYS * 24 * 60 * 60 * 1000);
    await updateDoc(doc(db, 'businesses', businessId), {
      lastReportGeneratedAt: serverTimestamp(),
      nextDeletionDate: nextDeletion.toISOString(),
      updatedAt: serverTimestamp(),
    });

    // ── 7. Delete old logs in batches (Firestore batch limit = 500) ──
    const toDelete = [...oldScans, ...oldClicks];
    for (let i = 0; i < toDelete.length; i += 400) {
      const batch = writeBatch(db);
      toDelete.slice(i, i + 400).forEach((d) => batch.delete(d.ref));
      await batch.commit();
    }

    console.log(
      `[retention] ✅ ${businessName}: report=${reportRef.id}, deleted ${oldScans.length} scans + ${oldClicks.length} clicks`
    );

    return {
      businessId,
      status: 'success',
      scansDeleted: oldScans.length,
      clicksDeleted: oldClicks.length,
      reportId: reportRef.id,
    };
  } catch (err) {
    console.error(`[retention] ❌ ${businessName}:`, err);
    return {
      businessId,
      status: 'error',
      scansDeleted: 0,
      clicksDeleted: 0,
      error: (err as Error).message,
    };
  }
}

// ─── Route handler ────────────────────────────────────────────────────────────

export async function GET(req: NextRequest) {
  if (!isAuthorized(req)) {
    return NextResponse.json({ error: 'Unauthorized' }, { status: 401 });
  }

  const startTime = Date.now();
  console.log('[retention] Cron job started at', new Date().toISOString());

  try {
    // Fetch all active businesses
    const bizSnap = await getDocs(
      query(collection(db, 'businesses'), where('active', '==', true))
    );

    if (bizSnap.empty) {
      return NextResponse.json({ message: 'No active businesses found', processed: 0 });
    }

    const results = [];

    // Process each business sequentially to avoid overwhelming Firestore
    for (const bizDoc of bizSnap.docs) {
      const data = bizDoc.data();
      const result = await processBusinessRetention(
        bizDoc.id,
        data.name || 'Unknown',
        data.ownerUid || ''
      );
      results.push(result);
    }

    const summary = {
      processed: results.length,
      success: results.filter((r) => r.status === 'success').length,
      skipped: results.filter((r) => r.status === 'skipped').length,
      errors: results.filter((r) => r.status === 'error').length,
      totalScansDeleted: results.reduce((s, r) => s + r.scansDeleted, 0),
      totalClicksDeleted: results.reduce((s, r) => s + r.clicksDeleted, 0),
      durationMs: Date.now() - startTime,
      timestamp: new Date().toISOString(),
    };

    console.log('[retention] Cron job completed:', summary);
    return NextResponse.json(summary);
  } catch (err) {
    console.error('[retention] Cron job failed:', err);
    return NextResponse.json(
      { error: 'Cron job failed', details: (err as Error).message },
      { status: 500 }
    );
  }
}

// Also support POST for manual triggers from dashboard
export async function POST(req: NextRequest) {
  return GET(req);
}
